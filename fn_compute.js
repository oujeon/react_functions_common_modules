"use strict";

/**
 * 더하기
 *
 * @param {*} str1
 * @param {*} str2
 * @returns
 */
export function plus(str1, str2) {
  return str1 + str2;
}
/**
 * 더하기
 *
 * @param {*} str1
 * @param {*} str2
 * @returns
 */
export function minus(str1, str2) {
  return str1 - str2;
}
/**
 * 곱하기
 *
 * @param {*} str1
 * @param {*} str2
 * @returns
 */
export function multiply(str1, str2) {
  return str1 * str2;
}
/**
 * 나누기
 *
 * @param {*} str1
 * @param {*} str2
 * @returns
 */
export function divide(str1, str2) {
  return str1 / str2;
}
